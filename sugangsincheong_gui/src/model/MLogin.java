package model;

public class MLogin {
	private String id;
	private String pw;
	private String userId;
	
	public MLogin() {
	
	}

	public String getID() {
		return id;
	}

	public void setID(String id) {
		this.id = id;
	}

	public String getPW() {
		return pw;
	}

	public void setPW(String pw) {
		this.pw = pw;
	}

	public String getUserId() {
		return userId;
	}

	public void setUserId(String userId) {
		this.userId = userId;
	}


}
