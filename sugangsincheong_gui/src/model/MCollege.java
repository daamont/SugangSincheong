package model;

//components
public class MCollege { 
	private int collegeNumber;
	private String name;
	private int campusNumber;
	
	public MCollege() {
	}
		
	public int getCollegeNumber() {
		return collegeNumber;
	}

	public void setCollegeNumber(int collegeNumber) {
		this.collegeNumber = collegeNumber;
	}

	public int getCampusNumber() {
		return campusNumber;
	}

	public void setCampusNumber(int campusNumber) {
		this.campusNumber = campusNumber;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}


}
